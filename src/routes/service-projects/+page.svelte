	<h1 class="sub-title">DTGB E-Club</h1>
	<p>
		The Dare to be Great E-Club is a vibrant community of change-makers with a global vision, driven
		by the profound belief in empowering young individuals to create audacious solutions for
		pressing world issues. Collaborating under the leadership of Mr. Bill Graham, the President of
		District 7475, the club facilitates a platform where the J.P. Stevens Interact Club actively
		participates in transformative initiatives. Notably, the annual Dare to be Great Competition
		challenges teams to conceive remarkably innovative solutions to major global problems. These
		proposals encompass intricate details, addressing how the solutions would function, their
		impact, implementation strategies, required resources, and organizational frameworks.
	</p>
	<p>
		Highlighting the remarkable impact of this collaboration, the J.P. Stevens Interact Club has
		consistently excelled in the Dare to be Great Competition. In 2019, three members from the club
		emerged victorious, earning the honor of presenting their project at the United Nations Summit
		in Mexico. In 2020, the club's exceptional prowess continued as two teams came first and second
		with their visionary proposals "Race Against E-Waste" and "Scrap to Tap" respectively.
		Facilitating these endeavors, regular meetings every other Saturday, conducted via Zoom and
		hosted by Mr. Graham, provides a dedicated space for mentorship, brainstorming, and fostering a
		culture of audacious problem-solving, contributing to the cultivation of future leaders and
		innovative thinkers.
	</p>
	<img src="/service-projects/dtgb.png" alt="DTGB Logo" />
