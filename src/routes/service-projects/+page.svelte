	<h1 class="sub-title">Hands of Hope</h1>
	<p>
		Hands of Hope, based in Edison, NJ, is a compassionate and impactful non-profit community
		organization dedicated to alleviating hunger and promoting human dignity. With a steadfast
		commitment to providing essential sustenance, the organization operates as an efficient and
		centralized system for distributing food to those in need. Founded in 1992, Hands of Hope
		initially began as a food pantry/soup kitchen and has evolved into a beacon of support within
		the community. Driven by an unwavering mission, Hands of Hope aims to ensure good nutrition,
		health, and overall well-being, allowing individuals to realize their full potential and escape
		the challenges hunger presents.
	</p>
	<p>
		The J.P. Stevens Interact Club proudly partners with Hands of Hope in their noble endeavor.
		Biweekly, on the 1st and 3rd Saturday of every month, our dedicated club members volunteer their
		time and efforts to contribute meaningfully to this vital cause. In collaboration with Hands of
		Hope, our Interact Club engages in various activities such as pantry stocking to maintain
		inventory, efficiently delivering food to cars through a drive-up model established during the
		COVID-19 pandemic, and providing crucial sustenance to individuals and families in need. Through
		these collective efforts, we aim to make a tangible and positive impact within our community,
		one act of service at a time.
	</p>
	<img src="/service-projects/hands-hope.png" alt="Hands Of Hope Logo" />
